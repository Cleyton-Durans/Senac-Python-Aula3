#   10.	Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Farenheit.

celsius = float(input("Digite um n. em graus celsius para ser calculado: "))

farenheit = (9 * celsius / 5) + 32
print(F'O Graus Celsius calculados é calulado em Farenheit  {farenheit:.2f} °C. ')


