# 4. Faça um Programa que peça as 4 notas bimestrais e mostre a méd
n1 = int(input("Vamos calcular sua média, Digite a primeira nota :"))
n2 = int(input("Vamos calcular sua média, Digite a primeira nota :"))
n3 = int(input("Vamos calcular sua média, Digite a primeira nota :"))
n4 = int(input("Vamos calcular sua média, Digite a primeira nota :"))
soma = (n1 + n2 + n3 + n4) / 4
print(f" Sua média é {soma}")