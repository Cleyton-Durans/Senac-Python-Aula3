# Entrada de dados
int1 = int(input("Digite o primeiro número inteiro: "))
int2 = int(input("Digite o segundo número inteiro: "))
real = float(input("Digite um número real:  "))

# Cálculos
produto = (2 * int1) * (int2 / 2)
soma = (3 * int1) + real
cubo = real ** 3

# Resultados
print(f"O produto do dobro do primeiro com metade do segundo: {produto}")
print(f"A soma do triplo do primeiro com o terceiro: {soma}")
print(f"O terceiro número elevado ao cubo: {cubo}")
