# REMOVENDO O VETOR PELO COMANDOS 'REMOVE' E 'POP()'
fruta = ['maça', 'banana', 'laranja', 'melão','morango']
print (f"Imprima vetor {fruta}")

# REMOVENDO PELO ÍNDICE USANDO 'POP()'
fruta.pop(1)
print(f"Imprima o vetor após atualização: {fruta}")

#REMOVENDO PELO 'REMOVE'
fruta.remove('melão')
print(f"Imrpima o vetor atualizado: {fruta}")